#include "libft.h"

int		ft_isalnum(int c)
{
	int	resp;

	if ((c >= '0' && c <= '9') ||
			(c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		resp = 1;
	else
		resp = 0;
	return (resp);
}
